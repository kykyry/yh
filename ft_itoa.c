#include "libft.h"

char	*ft_itoa(int n)
{
	char	*s;
	int		i;

	i = 1;
	s = malloc(sizeof(char) * 12);
	if (!s)
		return (0);
	s = s + 12;
	*s = '\0';
	if (n <= 0)
	{
		i = -1;
		n = -n;
	}
	while (n != 0)
	{
		*(--s) = (n % 10) + '0';
		n = n / 10;
	}
	if ((i == -1) && *s)
	{
		s--;
		*s = '-';
	}
	return (s);
}
